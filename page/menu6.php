<h2>Menu 6</h2>
저희는 화상영어 학습을 위해서 필리핀 현지에서 2006년 부터 2016년 현재까지 직접 화상 영어 콜센터를 운영하고 있습니다.
자 그럼, 영어 선생님들은 어떤 곳에서 수업을 하는지 한번 알아볼까요?


위 사진은 화상영어 콜센터에 2015년 11월 크리스마스 장식을 한 사진입니다.
아침 이른 시간에 찍은 사진이라 아직 매니저 선생님들과 영어 선생님들이 출근하기 전이어서 사무실이 비어 있는 것 같이 보입니다.




위 사진은 영어 선생님들의 스테이션(station)입니다.
선생님들은 자신의 자리에서 수업을 합니다.
주로 25분 단위로 수업을 하며 5분 쉬고 다시 수업을 합니다.
또한 하루에 30분짜리 휴식이 두개가 있어 식사 또는 쉬는 시간을 갖기도 합니다.